# operadores aritmeticos
# Division entera:
num1 = 10
num2 = 3
resultado = num1 / num2
print(resultado)  # Nos dara 3.3333333
resultado = num1 // num2
print(resultado)  # Nos dara 3
resultado = num1 % num2
print(resultado)  # Nos dara 1. Nos da el resto de la division
resultado = num1 ** num2
print(resultado)  # Nos dara 35 es decir 2 elevado a 5
# (exponencial o elevado a una potencia)
